    public DoSAttack[] getAttacks()
    {
        List<DoSAttack> list = new ArrayList<DoSAttack>();
        for ( Entry<JCheckBox, DoSAttack> entry : map.entrySet() )
        {
            if ( entry.getKey().isSelected() )
            {
                list.add( entry.getValue() );
            }
        }

        DoSAttack[] array = new DoSAttack[list.size()];

        return list.toArray( array );
    }

