    public ConfigureAttacksDialog_NB( DoSAttack[] attacks )
    {
        // set the title and center the dialog
        setTitle( "Configure DoS Attacks" );

        initComponents();

        JCheckBox[] checkboxes = { cbCoPa, cbElCo, cbAtCo, cbEnEx, cbExEn, cbHaCo, cbOvNa };
        JButton[] buttons = { btnCoPa, btnElCo, btnAtCo, btnEnEx, btnExEn, btnHaCo, btnOvNa };

        for ( int i = 0; i < defaultAttacks.length; i++ )
        {
            boolean found = false;
            for ( DoSAttack current : attacks )
            {
                if ( defaultAttacks[i].getClass().equals( current.getClass() ) )
                {
                    checkboxes[i].setSelected( true );
                    map.put( checkboxes[i], current );
                    mapBtn.put( buttons[i], current );
                    found = true;
                }
            }

            if ( !found )
            {
                map.put( checkboxes[i], defaultAttacks[i] );
                mapBtn.put( buttons[i], defaultAttacks[i] );
                buttons[i].setEnabled( false );
            }
        }

    }

